package entities;

public class Author {
    private int id;
    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "Author{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }

    public int hashCode(){
        int result=17;
        result+=5*result+Integer.hashCode(id);
        result+=5*result+hashCode();
        return result;
    }

}
